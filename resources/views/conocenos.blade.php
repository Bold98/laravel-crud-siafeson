                <h5>OBJETIVO GENERAL</h5>
                <p align="justify">
                    Establecer las metodologías y herramientas que ayuden en la integración de los diferentes tipos de
                    información necesaria para la aplicación e implementación a escala regional de los diversos modelos
                    fitosanitarios que sean aplicables al estado de Sonora.
                </p>
                <h5>OBJETIVOS ESPECÍFICOS</h5>
                <p align="justify">
                    Definir los principales problemas fitosanitarios en el Estado. Organizar, analizar y depurar en caso
                    de ser
                    necesario, la información meteorológica y fitosanitaria existente en el estado. Aplicación y
                    validación de
                    modelos fitosanitarios. Desarrollo del software necesario que permita integrar la información
                    existente con
                    la aplicación de los modelos. Publicación de resultados en línea (web). </p>
                <h5>¿QUIÉNES SOMOS?</h5>
                <p align="justify">
                    El Sistema de Alerta Fitosanitaria del Estado de Sonora (SIAFESON) es un proyecto que busca
                    proporcionar al
                    gremio agrícola del estado, las metodologías y herramientas que permitan minimizar los daños y
                    riesgos ante
                    una eventualidad (Heladas, surgimiento de plagas y/o enfermedades en los cultivos, etc.). Así como
                    también
                    la integración de los diferentes tipos de información necesaria para la implementación de los
                    diversos
                    modelos fitosanitarios aplicables en Sonora. Asimismo, se busca que los resultados de los modelos se
                    traduzcan en la emisión de un boletín de alerta fitosanitaria para el estado de Sonora. El boletín
                    de alerta
                    fitosanitaria como producto final de todo el sistema que en sí es el SIAFESON deberá ser una
                    herramienta que
                    apoye en el diseño y evaluación de las campañas fitosanitarias en el Estado. En resumen, SIAFESON
                    fue
                    concebido y diseñado para proporcionar a los productores agrícolas de Sonora las herramientas que
                    les
                    permitan minimizar daños y riesgos ante cualquier eventualidad (surgimiento de plagas y/o
                    enfermedades en
                    los cultivos) manteniendo un estatus de sanidad vegetal. </p>

